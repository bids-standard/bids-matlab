function content = return_empty_model()
  %
  % Creates the content of a basic model.json file for GLM analysis with
  % some default options like high pass filter cut-off.
  %
  % USAGE::
  %
  %   content = bids.model.return_empty_model()
  %
  % :returns:
  %
  % - :content: structure containing the output that can be saved with
  %             ``bids.util.jsonwrite()``. See below.
  %
  % EXAMPLE::
  %
  %   options.indent = '   ';
  %   content = bids.model.return_empty_model()
  %   filename = fullfile(pwd, 'models', 'model-empty_smdl.json')
  %   bids.util.jsonwrite(filename, content, options);
  %
  % (C) Copyright 2020 CPP_SPM developers

  content.Name = ' ';
  content.Description = ' ';
  content.Input = struct('task', ' ');
  content.Steps = { ...
                   struct( ...
                          'Level', 'subject', ...
                          'Transformations', {returnTransformation()}, ...
                          'Model', returnModel(), ...
                          'AutoContrasts', {{' '}}); ...
                   struct( ...
                          'Level', 'run', ...
                          'Transformations', {returnTransformation()}, ...
                          'Model', returnModel(), ...
                          'AutoContrasts', {{' '}} ...
                         ); ...
                   struct( ...
                          'Level', 'dataset', ...
                          'AutoContrasts', {{' '}} ...
                         ) ...
                  };

end

function transformation = returnTransformation()

  transformation =   {struct( ...
                             'Name', 'Factor', ...
                             'Inputs', {{'trial_type'}} ...
                            ); ...
                      struct( ...
                             'Name', 'Convolve', ...
                             'Model', '', ...
                             'Inputs', {{' '}} ...
                            )};

end

function model = returnModel()
  model = struct( ...
                 'X', {{' '}}, ...
                 'Options', struct('high_pass_filter_cutoff_secs', []), ...
                 'Software', struct('', struct('whitening', '')), ...
                 'Mask', {' '});
end
